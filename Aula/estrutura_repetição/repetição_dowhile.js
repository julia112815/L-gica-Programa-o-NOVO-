// Estrutura de repetição que executa pelo menos uma única vez.

for(let i =0; i < 2; i++){
    console.log(i)
}
console.log (`Fim do for`)

let y = 0 
while(y < 2){
    console.log(y)
    y++
}

console.log (`Fim DO...WHILE`)
let z = 0
do {
    console.log(z)
    z++
}while(z < 2)
